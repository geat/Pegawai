    private void cmdSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdSimpanActionPerformed
        // TODO add your handling code here:
        String cekNamaGolongan=txtNamaGolongan.getText();
        
        if(cekNamaGolongan.trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Nama Harus Di Isi!");
        }else{
            try{
        
                
                DaftarGolongan dG = new DaftarGolongan();
                dG.setNamaGolongan(txtNamaGolongan.getText());
                
                dGService.insert(dG);
                        this.statusAwal();
                        JOptionPane.showMessageDialog(this, "Data barang berhasil disimpan!");
            }
            catch(SQLException ex){
                Logger.getLogger(FormGolongan.class.getName()).log(Level.SEVERE, null, ex);
            }
        } 
        statusAwal();
    }//GEN-LAST:event_cmdSimpanActionPerformed

