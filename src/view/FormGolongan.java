    void statusAwal(){
        kosongkanText();
        loadData();
        isiTabel();
    }
     void loadData(){
       if("".equals(txtNamaGolonganCari.getText())){
         try{
            record = dGService.getAll();
        }
        catch(SQLException ex){
            Logger.getLogger(FormGolongan.class.getName()).log(Level.SEVERE, null, ex);
        }
       }else
       {
               try{
                    String namaGolongan = txtNamaGolonganCari.getText();
                    record = dGService.cariGolongan(namaGolongan);
        }
        catch(SQLException ex){
            Logger.getLogger(FormGolongan.class.getName()).log(Level.SEVERE, null, ex);
        }
       }
           
    }
     void isiTabel(){
         jTabelMasterGolongan.clearSelection();
        Object data[][] = new Object[record.size()][4];
        int x = 0;
        for(DaftarGolongan dG:record){
            data[x][0] = dG.getId();
            data[x][1] = dG.getNamaGolongan();
            x++;
        }
            String judul[] = {"Kode Golongan","Nama Golongan"};
        jTabelMasterGolongan.setModel(new DefaultTableModel(data, judul));
        jScrollPane1.setViewportView(jTabelMasterGolongan);
        setWidth();
    }
     
    void isiText(){
        DaftarGolongan dG = record.get(row);
        txtKodeGolongan.setText(dG.getId()+"");
        txtNamaGolongan.setText(dG.getNamaGolongan());
        
    }
    public final void setWidth() {
        jTabelMasterGolongan.getColumnModel().getColumn(0).setPreferredWidth(80);
        jTabelMasterGolongan.getColumnModel().getColumn(1).setPreferredWidth(250);
    }
   
 
